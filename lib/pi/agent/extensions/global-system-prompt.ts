const OVERRIDE_FILE = join(
  homedir(),
  ".pi",
  "agent",
  "global-system-prompt.md",
);

function loadPrompt(): string {
  try {
    const text = readFileSync(OVERRIDE_FILE, "utf8").trim();
    if (text) return text;
  } catch {
    // File doesn't exist or unreadable — fall back to default.
  }
  return DEFAULT_PROMPT;
}

export default function (pi: ExtensionAPI) {
  pi.on("before_agent_start", async (event) => {
    const extra = loadPrompt();
    if (!extra) return;
    return {
      // Append, preserving pi's chained prompt (custom prompt, AGENTS.md,
      // skills, tool snippets, and earlier extension changes).
      systemPrompt: `${event.systemPrompt}\n\n${extra}`,
    };
  });

  pi.registerCommand("global-prompt", {
    description: "Show the current global system prompt addendum",
    handler: async (_args, ctx) => {
      const extra = loadPrompt();
      const source =
        extra === DEFAULT_PROMPT ? "built-in default" : OVERRIDE_FILE;
      ctx.ui.notify(
        `Global system prompt (source: ${source}):\n\n${extra}`,
        "info",
      );
    },
  });
}
